    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        //$users = User::with('roles')->get();
        if ($request->ajax()) {
            $data = User::oldest()->get();
            return Datatables::of($data)
                    ->addIndexColumn()
                    ->addColumn('action', function($row){
                        $btn = '<a href="'.url("auth/users/" . $row->id ."/edit").'"
                           class="bs-tooltip text-center" data-toggle="tooltip" data-placement="top" title="Edit"
                           data-original-title="Edit">
                            <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="feather feather-edit"><path d="M11 4H4a2 2 0 0 0-2 2v14a2 2 0 0 0 2 2h14a2 2 0 0 0 2-2v-7"></path><path d="M18.5 2.5a2.121 2.121 0 0 1 3 3L12 15l-4 1 1-4 9.5-9.5z"></path></svg>
                           </a>';
                        $btn .= '<button type="button"
                           class="bs-tooltip text-center confirm-delete" data-id= "'.$row->id.'" data-toggle="tooltip" data-placement="top" title="Delete"
                           data-original-title="Edit">
                           <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="feather feather-x-circle table-cancel"><circle cx="12" cy="12" r="10"></circle><line x1="15" y1="9" x2="9" y2="15"></line><line x1="9" y1="9" x2="15" y2="15"></line></svg>
                           </button>';

                        return $btn;
                    })
                    ->addColumn('roles', function ($row) {
                        return $row->roles->count() ?
                        implode('
                        ', $row->roles->pluck('display_name')->toArray()) :
                        'NA';
                    })

                    ->rawColumns(['action'])
                    ->make(true);
        }

        return view('auth.users.index');
    }

